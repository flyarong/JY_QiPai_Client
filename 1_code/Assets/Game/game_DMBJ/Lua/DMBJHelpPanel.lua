local basefunc = require "Game/Common/basefunc"

DMBJHelpPanel = basefunc.class()
local C = DMBJHelpPanel
C.name = "DMBJHelpPanel"
local config = DMBJModel.dmbj_base_config.base
function C.Create()
	return C.New()
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:ctor()
	ExtPanel.ExtMsg(self)
	local parent = GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self:InitMainUI()
	self.close_btn.onClick:AddListener(
		function()
			ExtendSoundManager.PlaySound(audio_config.game.com_but_cancel.audio_name)
			self:MyExit()
		end
	)
	self:MyRefresh()
end

function C:MyRefresh()
end


function C:InitMainUI()
	local basestr = "link_"
	for i = 1,#config do
		local temp_ui = {}
		local b = GameObject.Instantiate(self.base,self.Content)
		b.gameObject:SetActive(true)
		LuaHelper.GeneratingVar(b.transform,temp_ui)
		temp_ui.item_img.sprite = DMBJPrefabManager.Prefabs["item_"..i]
		for j = 1,5 do
			local str = basestr..j
			if config[i][str] then
				temp_ui["link"..j.."_txt"].text = config[i][str]
			end
		end
	end
end