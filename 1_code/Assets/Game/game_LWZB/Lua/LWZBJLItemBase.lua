local basefunc = require "Game/Common/basefunc"

LWZBJLItemBase = basefunc.class()
local C = LWZBJLItemBase
C.name = "LWZBJLItemBase"

function C.Create(parent,data)
	return C.New(parent,data)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"] = basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent,data)
	self.data = data
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	for i=1,4 do
		self["bg"..i.."_img"].sprite = GetTexture(self:GetImgName(self.data[i]))
	end
	
	self:MyRefresh()
end

function C:MyRefresh()
end

function C:GetImgName(status)
	if status == 1 then 
		return "lwzb_icon_sheng"
	else
		return "lwzb_icon_fu"
	end
end