local basefunc = require "Game/Common/basefunc"

FishingMatchAwardPrefab = basefunc.class()
local C = FishingMatchAwardPrefab
C.name = "FishingMatchAwardPrefab"

function C.Create(parent_transform, config, call, panelSelf)
	return C.New(parent_transform, config, call, panelSelf)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:ctor(parent_transform, config, call, panelSelf)
	self.config = config
	self.call = call
	self.panelSelf = panelSelf
	local obj = newObject(C.name, parent_transform)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self:MyRefresh()
end

function C:MyRefresh()
	if self.config.award then
		if self.config.extra_award_desc then
			local dd = string.format("%.1f", self.config.extra_award_desc * 100)
			self.award_txt.text = self.config.award .. "+额外" .. dd .. "%的奖池金"
		else
			self.award_txt.text = self.config.award
		end
	else
		if self.config.extra_award_desc then
			local dd = string.format("%.1f", self.config.extra_award_desc * 100)
			self.award_txt.text = "" .. dd .. "%的奖池金"
		else
			self.award_txt.text = ""
		end
	end
	self.rank_txt.text = self.config.rank
end
