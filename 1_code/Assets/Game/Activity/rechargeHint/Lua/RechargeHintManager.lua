	if result == 0 then
        -- 数据初始化
	end
end
function M.OnReConnecteServerSucceed()
end

function M.on_PayTypePopPrefab_create_msg()
    local dayData = GameTaskModel.GetTaskDataByID(dayRecharge)
    if dayData and dayData.now_process > 2000000 then
        HintPanel.Create(1,"您今日充值较多，请注意理性消费。")
    end
end
    
function M.on_finish_gift_shop()
    local weekData = GameTaskModel.GetTaskDataByID(weekRecharge)
    if weekData and weekData.now_process > 5000000 then
        HintPanel.Create(1,"您本周的充值较多，请注意理性消费。")
    end
end