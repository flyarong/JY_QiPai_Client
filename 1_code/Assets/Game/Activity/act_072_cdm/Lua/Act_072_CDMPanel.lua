local basefunc = require "Game/Common/basefunc"

Act_072_CDMPanel = basefunc.class()
local C = Act_072_CDMPanel
C.name = "Act_072_CDMPanel"
local M = Act_072_CDMManager

local config = {}

function C.Create(parent)
	return C.New(parent)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
    if self.remain_time_timer then
        self.remain_time_timer:Stop()
    end
    self:DelItem()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent)
	ExtPanel.ExtMsg(self)
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
    config = M.GetConfig()
    self.remain_time_timer = CommonTimeManager.GetCutDownTimer(M.e_time,self.remain_txt)
	self:MyRefresh()
end

function C:MyRefresh()
    self:CreateItem()
end

function C:CreateItem()
    self:DelItem()
    for i=1,#config do
        local pre = Act_072_CDMItemBase.Create(self.Content.transform,config[i],self)
        self.pre_cell[#self.pre_cell + 1] = pre
    end
end

function C:DelItem()
    if self.pre_cell then
        for k,v in pairs(self.pre_cell) do
            v:MyExit()
        end
    end
    self.pre_cell = {}
end