local basefunc = require "Game/Common/basefunc"

Act_020_JBBXEnterPrefab = basefunc.class()
local C = Act_020_JBBXEnterPrefab
C.name = "Act_020_JBBXEnterPrefab"
local M = Act_020_JBBXManager
function C.Create(parent)
	return C.New(parent)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:OnDestroy()
	self:MyExit()
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:ctor(parent)
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	obj.transform.localPosition = Vector3.New(-580,-350,0)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	EventTriggerListener.Get(self.enter_btn.gameObject).onClick = basefunc.handler(self, self.on_Click)
	CommonHuxiAnim.Start(self.gameObject,1)
	self:MyRefresh()
end

function C:MyRefresh()
end

function C:on_Click()
	Act_020_JBBXPanel.Create()
end