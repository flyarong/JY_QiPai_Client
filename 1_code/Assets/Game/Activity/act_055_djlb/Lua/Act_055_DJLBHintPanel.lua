local basefunc = require "Game/Common/basefunc"

Act_055_DJLBHintPanel = basefunc.class()
local C = Act_055_DJLBHintPanel
local M = Act_055_DJLBManager
C.name = "Act_055_DJLBHintPanel"

function C.Create(index)
	return C.New(index)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(index)
	ExtPanel.ExtMsg(self)
	local parent = GameObject.Find("Canvas/LayerLv5").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	self.index = index
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self.confirm_btn.onClick:AddListener(function()
		Act_055_DJLBBuyPanel.Create()
		self:MyExit()
	end)

	self.close_btn.onClick:AddListener(function()
		self:MyExit()
	end)
	self.node1.gameObject:SetActive(self.index == 1)
	self.node2.gameObject:SetActive(self.index == 2)
	self:MyRefresh()
end

function C:MyRefresh()
end
