local basefunc = require "Game/Common/basefunc"

ACT_074_TCXBHintPanel = basefunc.class()
local C = ACT_074_TCXBHintPanel
C.name = "ACT_074_TCXBHintPanel"

function C.Create(str,img)
	return C.New(str,img)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"] = basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(str,img)
	ExtPanel.ExtMsg(self)
    self.str = str
    self.img = img
	local parent = GameObject.Find("Canvas/LayerLv5").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
    self.confirm_btn.onClick:AddListener(
        function()
            ExtendSoundManager.PlaySound(audio_config.game.com_but_cancel.audio_name)
            self:OnConfirmClick()
        end
    )
	self:MyRefresh()
end

function C:MyRefresh()
    self.hint_info_txt.text = self.str
    self.sw_img.sprite = GetTexture(self.img)
end

function C:OnConfirmClick()
    UniClipboard.SetText("4008882620")
    LittleTips.Create("复制成功~")
    self:MyExit()
end