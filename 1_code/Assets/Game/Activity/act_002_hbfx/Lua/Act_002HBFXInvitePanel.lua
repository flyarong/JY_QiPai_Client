local basefunc = require "Game/Common/basefunc"

Act_002HBFXInvitePanel = basefunc.class()
local C = Act_002HBFXInvitePanel
C.name = "Act_002HBFXInvitePanel"
local M = Act_002HBFXManager
function C.Create()
	return C.New()
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)

	 
end

function C:ctor()

	ExtPanel.ExtMsg(self)

	local parent = GameObject.Find("Canvas/LayerLv4").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self.close_btn.onClick:AddListener(
		function()
			self:MyExit()
		end
	)
	self.start_btn.onClick:AddListener(
		function ()
			M.goMatch()
		end
	)
	if M.getMasterInfo() then 
		self.tips_txt.text = "您的好友【"..M.getMasterInfo().name.."】"
	end 
	self:MyRefresh()
end

function C:MyRefresh()
end
