local basefunc = require "Game/Common/basefunc"

Sys_013_ZDKPLBPanel = basefunc.class()
local C = Sys_013_ZDKPLBPanel
C.name = "Sys_013_ZDKPLBPanel"

function C.Create()
	return C.New()
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
	self.lister = {}
	self.lister["AssetChange"] = basefunc.handler(self, self.OnAssetChange)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:ctor()
	local parent = GameObject.Find("Canvas/LayerLv5").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self.close_btn.onClick:AddListener(
		function ( )
			self:MyExit()
		end
	)
	self.buy1_btn.onClick:AddListener(
		function ()
			self:BuyShop(10257)
		end
	)
	self.buy2_btn.onClick:AddListener(
		function ()
			self:BuyShop(10258)
		end
	)
	self:MyRefresh()
end

function C:MyRefresh()
end


function C:BuyShop(shopid)
    local gb =  MainModel.GetShopingConfig(GOODS_TYPE.gift_bag, shopid)
    if not gb then return end
	local price = gb.price
	if  GameGlobalOnOff.PGPay and gameRuntimePlatform == "Ios" then
		ServiceGzhPrefab.Create({desc="请前往公众号获取"})
	else
		PayTypePopPrefab.Create(shopid, "￥" .. (price / 100))
	end
end

function C:OnAssetChange(data)
	if data.change_type and (data.change_type == "buy_gift_bag_10258" or data.change_type == "buy_gift_bag_10257")  then
		self:MyExit()
	end
end