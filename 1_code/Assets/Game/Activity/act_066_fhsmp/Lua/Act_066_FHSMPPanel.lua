local basefunc = require "Game/Common/basefunc"

Act_066_FHSMPPanel = basefunc.class()
local C = Act_066_FHSMPPanel
C.name = "Act_066_FHSMPPanel"

local M = Act_066_FHSMPManager

function C.Create()
	return C.New()
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
	self.lister["finish_gift_shop"] = basefunc.handler(self, self.on_finish_gift_shop)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor()
	ExtPanel.ExtMsg(self)
	local parent = GameObject.Find("Canvas/LayerLv4").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()

	self.back_btn.onClick:AddListener(function()
		self:MyExit()
	end)
	self.buy_btn.onClick:AddListener(function()
		GameManager.BuyGift(M.shop_id)
	end)
	self:MyRefresh()
end

function C:MyRefresh()
	self.buy_btn.gameObject:SetActive(M.IsCanBuyShop())
end

function C:on_finish_gift_shop()
	self:MyRefresh()
end
