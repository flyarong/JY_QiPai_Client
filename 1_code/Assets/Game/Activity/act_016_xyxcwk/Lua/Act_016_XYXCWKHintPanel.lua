local basefunc = require "Game/Common/basefunc"

Act_016_XYXCWKHintPanel = basefunc.class()
local C = Act_016_XYXCWKHintPanel
C.name = "Act_016_XYXCWKHintPanel"

function C.Create(index)
	return C.New(index)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:ctor(index)
	local parent = GameObject.Find("Canvas/LayerLv5").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	for i = 1,2 do
		self["node"..i].gameObject:SetActive(false)
	end
	self["node"..index].gameObject:SetActive(true)
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self.close_btn.onClick:AddListener(
		function ()
			self:MyExit()
		end
	)
	self.confirm_btn.onClick:AddListener(
		function ()
			self:MyExit()
			Act_016_XYXCWKLBPanel.Create()
		end
	)
	self:MyRefresh()
end

function C:MyRefresh()
end
